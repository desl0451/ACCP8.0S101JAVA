package cn.jbit.test;

public class Test3 {

	public static void main(String[] args) {
		// 张三为他的手机设定了自动拨号
		// 按1：拨爸爸的号
		// 按2：拨妈妈的号
		// 按3：拨爷爷的号
		// 按4：拨奶奶的号
		int phone = 3;
		switch (phone) {
		case 1:
			System.out.println("拨爸爸的号");
			break;
		case 2:
			System.out.println("拨妈妈的号");
			break;
		case 3:
			System.out.println("拨爷爷的号");
			break;
		case 4:
			System.out.println("拨奶奶的号");
			break;
		}

	}

}
